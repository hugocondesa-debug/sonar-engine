@pytest.fixture
def tmp_damodaran_cache(tmp_path: Path) -> Path:
    d = tmp_path / "damodaran_cache"
    d.mkdir()
    return d


@pytest.fixture
async def damodaran_connector(tmp_damodaran_cache: Path) -> AsyncIterator[DamodaranConnector]:
    conn = DamodaranConnector(cache_dir=str(tmp_damodaran_cache))
    yield conn
    await conn.aclose()


async def test_fetch_monthly_resolves_latest_on_hit(
    httpx_mock: HTTPXMock, damodaran_connector: DamodaranConnector
) -> None:
    """Target month published on first try → single download."""
    body = _build_synthetic_monthly_xlsx()
    httpx_mock.add_response(
        method="GET",
        url="https://pages.stern.nyu.edu/~adamodar/pc/implprem/ERPFeb26.xlsx",
        content=body,
    )
    row = await damodaran_connector.fetch_monthly_implied_erp(2026, 2)
    assert row is not None
    assert row.start_of_month == date(2026, 2, 1)
    assert row.source_file == "ERPFeb26.xlsx"
    # Cache hit on second call — no extra download.
    row2 = await damodaran_connector.fetch_monthly_implied_erp(2026, 2)
    assert row2 is not None
    # Two historical months in synthetic body — same file serves both.
    row_jan = await damodaran_connector.fetch_monthly_implied_erp(2026, 1)
    assert row_jan is not None
    assert row_jan.source_file == "ERPFeb26.xlsx"


async def test_fetch_monthly_walks_backward_on_404(
    httpx_mock: HTTPXMock, damodaran_connector: DamodaranConnector
) -> None:
    """Target month 404 → walk backward until a published file is found."""
    httpx_mock.add_response(
        method="GET",
        url="https://pages.stern.nyu.edu/~adamodar/pc/implprem/ERPApr26.xlsx",
        status_code=404,
    )
    httpx_mock.add_response(
        method="GET",
        url="https://pages.stern.nyu.edu/~adamodar/pc/implprem/ERPMar26.xlsx",
        status_code=404,
    )
    httpx_mock.add_response(
        method="GET",
        url="https://pages.stern.nyu.edu/~adamodar/pc/implprem/ERPFeb26.xlsx",
        content=_build_synthetic_monthly_xlsx(),
    )
    row = await damodaran_connector.fetch_monthly_implied_erp(2026, 4)
    # File found is Feb 2026; target month Apr 2026 absent from workbook → None.
    assert row is None
    # But Feb 2026 is present in that workbook.
    row_feb = await damodaran_connector.fetch_monthly_implied_erp(2026, 2)
    assert row_feb is not None
    assert row_feb.source_file == "ERPFeb26.xlsx"


async def test_fetch_monthly_returns_none_when_lookback_exhausted(
    httpx_mock: HTTPXMock, damodaran_connector: DamodaranConnector
) -> None:
    """All lookback candidates 404 → resolver gives up, returns None."""
    for url in (
        "https://pages.stern.nyu.edu/~adamodar/pc/implprem/ERPApr26.xlsx",
        "https://pages.stern.nyu.edu/~adamodar/pc/implprem/ERPMar26.xlsx",
        "https://pages.stern.nyu.edu/~adamodar/pc/implprem/ERPFeb26.xlsx",
        "https://pages.stern.nyu.edu/~adamodar/pc/implprem/ERPJan26.xlsx",
        "https://pages.stern.nyu.edu/~adamodar/pc/implprem/ERPDec25.xlsx",
        "https://pages.stern.nyu.edu/~adamodar/pc/implprem/ERPNov25.xlsx",
    ):
        httpx_mock.add_response(method="GET", url=url, status_code=404)
    assert await damodaran_connector.fetch_monthly_implied_erp(2026, 4) is None


async def test_fetch_monthly_rejects_bad_month(
    damodaran_connector: DamodaranConnector,
) -> None:
    with pytest.raises(ValueError, match="month out of range"):
        await damodaran_connector.fetch_monthly_implied_erp(2026, 13)
    with pytest.raises(ValueError, match="month out of range"):
        await damodaran_connector.fetch_monthly_implied_erp(2026, 0)


@pytest.mark.slow
@pytest.mark.filterwarnings("ignore::UserWarning")
async def test_live_canary_damodaran_monthly_implied_erp(
    tmp_damodaran_cache: Path,
) -> None:
    """Live probe against Damodaran NYU host — latest published file.

    Asserts the February 2026 row is within the [3.0 %, 6.0 %] band
    — a generous guard that catches both 0 (parser miss) and 10+
    (catastrophic schema drift). Also asserts ``source_file`` starts
    with ``ERP`` and ends ``.xlsx`` so URL-template drift triggers.

    Skips when ``DAMODARAN_LIVE_CANARY=0`` or unset only in CI; runs
    freely in the sprint run since the file is static monthly.
    """
    if os.environ.get("DAMODARAN_LIVE_CANARY") == "0":
        pytest.skip("DAMODARAN_LIVE_CANARY=0")
    conn = DamodaranConnector(cache_dir=str(tmp_damodaran_cache))
    try:
        # Feb 2026 is the current latest-published month per Sprint B
        # probe (2026-04-22). If Damodaran publishes a newer one the
        # resolver will prefer it; either way Feb 2026 must be inside
        # the workbook since every monthly file carries full history.
        row = await conn.fetch_monthly_implied_erp(2026, 2)
        assert row is not None
        assert row.source_file.startswith("ERP")
        assert row.source_file.endswith(".xlsx")
        assert 0.03 <= row.implied_erp_decimal <= 0.06
        assert row.sp500_level > 4000
        assert 0.02 <= row.tbond_rate_decimal <= 0.07
    finally:
        await conn.aclose()
