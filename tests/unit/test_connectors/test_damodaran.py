def _build_synthetic_monthly_xlsx(
    *,
    months: list[tuple[datetime, float | None, float | None, float, float]] | None = None,
    include_sustainable_col: bool = True,
    sheet_name: str = IMPLPREM_SHEET,
) -> bytes:
    """Build a synthetic implprem workbook.

    ``months`` is a list of ``(start_of_month_dt, erp_sust, erp_t12m,
    sp500, tbond)`` tuples. When ``erp_sust`` is ``None`` the sustainable
    column stays blank for that row (pre-2011 pattern).
    """
    wb = openpyxl.Workbook()
    ws = wb.active
    ws.title = sheet_name

    # 2 meta rows then header.
    ws.append(["Damodaran Implied ERP Monthly"])
    ws.append(["Generated by tests"])
    header = [
        COL_MONTH_START,
        COL_MONTH_SP500,
        COL_MONTH_TBOND_RATE,
        "$ Riskfree Rate",
        "Ten-year average CF",
        "CF (Trailing 12 month)",
        "Normalized CF",
        "Expected growth rate",
    ]
    if include_sustainable_col:
        header.append(COL_MONTH_ERP_SUSTAINABLE)
    header.append(COL_MONTH_ERP_T12M)
    ws.append(header)

    # Damodaran's monthly file stores start-of-month as a naive datetime
    # in the xlsx cell — openpyxl refuses to serialise tz-aware datetimes,
    # so the test fixture must mirror the on-wire format.
    rows = (
        months
        if months is not None
        else [
            (datetime(2026, 1, 1), 0.0418, 0.0423, 6846.0, 0.0418),  # noqa: DTZ001
            (datetime(2026, 2, 1), 0.0417, 0.0425, 6939.0, 0.0426),  # noqa: DTZ001
        ]
    )
    for dt, erp_sust, erp_t12m, sp500, tbond in rows:
        row: list[object] = [dt, sp500, tbond, None, None, None, None, None]
        if include_sustainable_col:
            row.append(erp_sust)
        row.append(erp_t12m)
        ws.append(row)

    buf = io.BytesIO()
    wb.save(buf)
    return buf.getvalue()


class TestParseMonthly:
    def test_prefers_sustainable_column(self) -> None:
        body = _build_synthetic_monthly_xlsx()
        result = _parse_monthly(body, 2026, 2, source_file="ERPFeb26.xlsx")
        assert isinstance(result, DamodaranMonthlyERPRow)
        assert result.start_of_month == date(2026, 2, 1)
        assert result.implied_erp_decimal == pytest.approx(0.0417)
        assert result.implied_erp_t12m_decimal == pytest.approx(0.0425)
        assert result.sp500_level == pytest.approx(6939.0)
        assert result.tbond_rate_decimal == pytest.approx(0.0426)
        assert result.source_file == "ERPFeb26.xlsx"

    def test_falls_back_to_t12m_when_sustainable_missing(self) -> None:
        body = _build_synthetic_monthly_xlsx(
            months=[
                (datetime(2010, 5, 1), None, 0.0533, 1100.0, 0.032),  # noqa: DTZ001
            ],
        )
        result = _parse_monthly(body, 2010, 5, source_file="ERPMay10.xlsx")
        assert result is not None
        assert result.implied_erp_decimal == pytest.approx(0.0533)

    def test_returns_none_when_both_erp_columns_null(self) -> None:
        body = _build_synthetic_monthly_xlsx(
            months=[(datetime(2008, 9, 1), None, None, 1252.0, 0.0372)],  # noqa: DTZ001
        )
        assert _parse_monthly(body, 2008, 9, source_file="ERPSep08.xlsx") is None

    def test_returns_none_when_month_absent(self) -> None:
        body = _build_synthetic_monthly_xlsx()
        assert _parse_monthly(body, 2025, 7, source_file="ERPJul25.xlsx") is None

    def test_returns_none_when_sheet_missing(self) -> None:
        body = _build_synthetic_monthly_xlsx(sheet_name="Wrong Sheet")
        assert _parse_monthly(body, 2026, 2, source_file="ERPFeb26.xlsx") is None


